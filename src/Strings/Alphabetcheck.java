package Strings;

public class Alphabetcheck {
        public static void main(String[] args) {
            char ch = '1';

            if ((ch >= 'a' && ch <= 'z') || (ch >= 'A' && ch <= 'Z')) {
                System.out.println("Alphabet");
            } else {
                System.out.println("Not Alphabet");
            }
        }
    }


